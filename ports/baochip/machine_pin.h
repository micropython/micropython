#ifndef MICROPY_INCLUDED_BAOCHIP_MACHINE_PIN_H
#define MICROPY_INCLUDED_BAOCHIP_MACHINE_PIN_H

#include "py/obj.h"

#define BAOCHIP_NUM_PORTS       (6)
#define BAOCHIP_PINS_PER_PORT   (16)

typedef struct _machine_pin_obj_t {
    mp_obj_base_t base;
    qstr_short_t name;
    uint8_t port;
    uint8_t pin;
} machine_pin_obj_t;

// Open-drain emulation state: one bit per pin, one uint16_t per port.
extern uint16_t machine_pin_open_drain_mask[BAOCHIP_NUM_PORTS];

#define PIN_IS_OPEN_DRAIN(port, pin)     (machine_pin_open_drain_mask[(port)] & (1u << (pin)))
#define PIN_ENABLE_OPEN_DRAIN(port, pin) (machine_pin_open_drain_mask[(port)] |= (1u << (pin)))
#define PIN_DISABLE_OPEN_DRAIN(port, pin) (machine_pin_open_drain_mask[(port)] &= ~(1u << (pin)))

// Generated by make-pins.py: one ROM object per pin used by this board.
extern const mp_obj_dict_t machine_pin_cpu_pins_locals_dict;
extern const mp_obj_dict_t machine_pin_board_pins_locals_dict;

const machine_pin_obj_t *machine_pin_find(mp_obj_t pin);

// Pull in generated externs and #define pin_XXX pointer aliases.
#include "genhdr/pins.h"

#endif // MICROPY_INCLUDED_BAOCHIP_MACHINE_PIN_H
